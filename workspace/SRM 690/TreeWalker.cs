public class TreeWalker
{
    public int calc(int N, int A0, int B, int C, int MOD)
    {
        var G = Enumerate(N, x => new List<int>());
        var A = new long[N];
        A[0] = A0;
        for (int i = 1; i < N; i++)
            A[i] = (A[i - 1] * B + C) % MOD;
        for (int i = 1; i < N; i++)
        {
            var j = (int)(A[i - 1] % i);
            G[j].Add(i);
            //G[j].Add(i);
        }
        ModInteger ans = 0;
        ModInteger INV2 = ModInteger.Inverse(2);
        var dp = new ModInteger[N];
        for (int i = 0; i < N; i++)
            dp[i].num = -1;
        Func<int, int, ModInteger> dfs = null;
        dfs = (prev, cur) =>
          {
              if (dp[cur].num >= 0) return dp[cur];
              //Debug.WriteLine("{0} {1}", prev, cur);
              ModInteger ret = 0;
              foreach (var to in G[cur])
              {
                  var res = dfs(cur, to) * INV2;
                  ans += res;
                  ans += ret * res;
                  ret += res;
              }
              return dp[cur] = ret + 1;
          };
        for (int i = N - 1; i >= 0; i--)
            dfs(-1, i);
        ans *= ModInteger.Pow(2, N);
        return (int)ans.num;
    }

    static public T[] Enumerate<T>(int n, Func<int, T> f) { var a = new T[n]; for (int i = 0; i < n; ++i) a[i] = f(i); return a; }
    static public void Swap<T>(ref T a, ref T b) { var tmp = a; a = b; b = tmp; }
}
