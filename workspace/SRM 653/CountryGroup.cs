public class CountryGroup
{
    public int solve(int[] a)
    {
        var cnt = 0;
        for (int i = 0; i < a.Length;)
        {
            var k = a[i];
            for (int j = 0; j < k; j++)
            {
                if (i + j >= a.Length || a[i + j] != k) return -1;
            }
            i += k;
            cnt++;
        }

        return cnt;
    }

